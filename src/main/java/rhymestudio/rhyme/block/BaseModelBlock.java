public class BaseModelBlock extends Block  {

    public BaseModelBlock(Properties properties) {
        super(properties);
    }

    public static final MapCodec<BaseModelBlock> CODEC = simpleCodec(BaseModelBlock::new);


    @Override
    protected @NotNull MapCodec<? extends Block> codec() {
        return CODEC;
    }

    public void fallOn(@NotNull Level level, @NotNull BlockState state, @NotNull BlockPos pos, net.minecraft.world.entity.Entity entity, float fallDistance) {
        if (entity.isSuppressingBounce()) {
            super.fallOn(level, state, pos, entity, fallDistance);
        }
    }

    @Override
    protected RenderShape getRenderShape(BlockState state) {
        return RenderShape.MODEL;
    }

}
